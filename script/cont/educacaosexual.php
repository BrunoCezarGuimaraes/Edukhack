<br><h1>Educação Sexual</h1><br>
<div class="box">
   	<p>A gravidez não planejada é muitas vezes o motivo de evasão escolar tanto no ensino fundamental e médio quanto no ensino superior. Para prevenir a gravidez indesejada, a educação sexual se torna essencial. Além dessa função, esse tipo de conhecimento também ajuda a preservar a saúde, uma vez que previne Doenças Sexualmente Transmissíveis (DSTs).</p>

